#ifndef CHECKASM_PERF_GETTIME_H
#define CHECKASM_PERF_GETTIME_H

#include <stdint.h>
#include <time.h>

static inline uint64_t clock_gettime_nsec(void) {
    struct timespec ts;
#ifdef CLOCK_MONOTONIC_RAW
    clock_gettime(CLOCK_MONOTONIC_RAW, &ts);
#else
    clock_gettime(CLOCK_MONOTONIC, &ts);
#endif
    return ((uint64_t)ts.tv_sec*1000000000u) + (uint64_t)ts.tv_nsec;
}

#define PERF_SETUP()
#define PERF_START(t) t = clock_gettime_nsec();
#define PERF_STOP(t)  t = clock_gettime_nsec() - t
#define PERF_NAME     "clock_gettime"

#endif /* CHECKASM_PERF_GETTIME_H */
